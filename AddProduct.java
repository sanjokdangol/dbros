    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed

        Connect connect = new Connect();
        Connection con = connect.getConnect();
        PreparedStatement pmst = null;

        try {

            String sql = "INSERT INTO products(product_name, price) values(?,?)";
            pmst = con.prepareStatement(sql);

            pmst.setString(1, name.getText());
            pmst.setString(2, price.getText());

            int res = pmst.executeUpdate();

            if (res > 0) {
                clear();
                getTable();
                JOptionPane.showMessageDialog(null, "Successfully Added,Success Alert");
                deletebtn.setEnabled(false);
                updatebtn.setEnabled(false);
                clearbtn.setEnabled(false);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);

        } catch (Exception ex) {
            System.out.println(ex);
        }


    }//GEN-LAST:event_addbtnActionPerformed

    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        PreparedStatement pmst = null;
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Would You Like to Delete " + getId() + " Parmanently?", "Warning", dialogButton);
        if (dialogResult == JOptionPane.YES_OPTION) {
            try {

                Connect connect = new Connect();
                Connection con = connect.getConnect();
                String sql = "DELETE FROM products WHERE id=?";

                pmst = con.prepareStatement(sql);
                pmst.setInt(1, AddProduct.getId());
                int response = pmst.executeUpdate();

                if (response > 0) {
                    getTable();
                    clear();
                    JOptionPane.showMessageDialog(null, "Successfully Deleted,Success Alert");
                }

            } catch (Exception ex) {
                System.out.println(ex);
            }
        }


    }//GEN-LAST:event_deletebtnActionPerformed

    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
  
            PreparedStatement pmst = null;
            Connect connect = new Connect();
            Connection con = connect.getConnect();
            try {

                String sql = "UPDATE products SET product_name=?, price=? WHERE id=?";
                pmst = con.prepareStatement(sql);
                pmst.setString(1, name.getText());
                pmst.setInt(2, Integer.parseInt(price.getText()));
                pmst.setInt(3, getId());
                
                int response = pmst.executeUpdate();
                if (response > 0) {
                    getTable();
                    JOptionPane.showMessageDialog(null, "Successfully Updated, Success Alert!");
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "update error: " + e);
            }

   
    }//GEN-LAST:event_updatebtnActionPerformed

