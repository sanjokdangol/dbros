    public void getTable() {
        dtm = (DefaultTableModel) tbl.getModel();
        dtm.setRowCount(0);

        Connect con = new Connect();
        Connection cn = con.getConnect();
        String sqltbl = "select id,product_name,price from products";
        try {
            PreparedStatement pmst = cn.prepareStatement(sqltbl);
            ResultSet rs = pmst.executeQuery();
            while (rs.next()) {
                dtm.insertRow(dtm.getRowCount(), new Object[]{
                    rs.getString(1), rs.getString(2), rs.getString(3)

                });

            }

        } catch (Exception e) {
            System.out.println("table err: " + e);
        }
    }

